// Build the TRANSLATIONS[from][to][phrase] lookup from PHRASE_DB
// This creates a reverse index so any source language phrase can be looked up
const LANG_CODES = ['en','es','zh','ar','hi','pt','fr','de','ja','ko','vi','tl','ru','pl','uk','sw','ha','am','ht','th','tr','id'];

const TRANSLATIONS = {};

// For each source language, build a map of (source phrase → English key)
// Then for each target language, map (source phrase → target phrase)
(function buildTranslations() {
  // First, build reverse index: for each lang, map localized phrase → English key
  const reverseIndex = {};
  for (const lang of LANG_CODES) {
    reverseIndex[lang] = {};
    for (const [enPhrase, translations] of Object.entries(PHRASE_DB)) {
      if (translations[lang]) {
        reverseIndex[lang][translations[lang]] = enPhrase;
      }
    }
  }

  // Now build TRANSLATIONS[from][to][sourcePhrase] = targetPhrase
  for (const fromLang of LANG_CODES) {
    TRANSLATIONS[fromLang] = {};
    for (const toLang of LANG_CODES) {
      if (fromLang === toLang) continue;
      TRANSLATIONS[fromLang][toLang] = {};
      for (const [enPhrase, translations] of Object.entries(PHRASE_DB)) {
        const sourcePhrase = translations[fromLang];
        const targetPhrase = translations[toLang];
        if (sourcePhrase && targetPhrase) {
          TRANSLATIONS[fromLang][toLang][sourcePhrase] = targetPhrase;
        }
      }
    }
  }
})();
